    // method for pattern 22
    public void pat22(int n22) {
        for (int i = 0; i < 2 * n22 - 1; i++) {
            for (int j = 0; j < 2 * n22 - 1; j++) {
                // distance from top
                int top = i;
                // distance from left
                int left = j;
                // distance from right
                int right = (2 * n22 - 2) - j;
                // distance from down
                int down = (2 * n22 - 2) - i;
                // minimum of all four distance
                int minDist = Math.min(Math.min(top, down), Math.min(left, right));
                System.out.print((n22 - minDist) + " ");
            }
            System.out.println();
        }
    }
